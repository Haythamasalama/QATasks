public class User {
    private String email;
    private String name;

    public String getName() {
        return this.name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String setEmail(String email) {
        return this.email = email;
    }

    public String getEmail() {
        return this.email;
    }
}
